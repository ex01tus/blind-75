package math;

/**
 * Description: https://leetcode.com/problems/bulb-switcher
 * Difficulty: Medium
 * Time complexity: O(1)
 * Space complexity: O(1)
 */
public class BulbSwitcher {

    public int bulbSwitch(int n) {
        // this is a pure math problem, so feel free to read the explanation in the Solutions section on LeetCode
        return (int) Math.sqrt(n);
    }
}
